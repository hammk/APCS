// Humzah Khan's amazing first-try quote-inator.	

public class QuoteTester {

	public static void main(String[] args) {
		// Multiple Println lines for quote lines
		
		System.out.println("A well-formed Java program has");
		System.out.println("A main method with { and }");
		System.out.println("braces.\n");
		System.out.println("A System.out.println statement");
		System.out.println("Has ( and ) and usually a");
		System.out.println("String that stands and ends");
		System.out.println("with a \" character.\n");
		System.out.println("Most of all, a well-formed Java program has");
		System.out.println("Should have // comments.");
		
		System.out.println(""); // blank line for the next print statement
		
		// A single Print line for one quote line and newlines.
		
		System.out.print("A well-formed Java program has \nA main method with { and } \nbraces. \n \nA System.out.println statement \nHas ( and ) and usually a \nString that starts and ends with a \" character. \n \nMost of all, a well-formed Java program \nShould have // comments.");
		
	}

}
